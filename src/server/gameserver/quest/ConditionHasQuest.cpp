////////////////////////////////////////////////////////////////////////////////
void ConditionHasQuest::read (PropertyBuffer & propertyBuffer) 
	
{
	try
	{
		m_QuestID = propertyBuffer.getPropertyInt("QuestID");
	}
	catch (NoSuchElementException & nsee)
	{
		throw Error(nsee.toString());
	}
}

