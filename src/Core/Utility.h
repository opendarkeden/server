//////////////////////////////////////////////////////////////////////////////
// 다이스 함수
//////////////////////////////////////////////////////////////////////////////
inline uint Dice(uint num, uint dice)
{
    uint result = 0;
	for(uint i = 0 ; i < num ; i ++)
		result += rand() % dice + 1;
	return result;
}


